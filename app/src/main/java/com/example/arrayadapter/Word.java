package com.example.arrayadapter;

public class Word {
    String uppperWord;
    String lowerWord;

    public Word() {
    }

    public Word(String uppperWord, String lowerWord) {
        this.uppperWord = uppperWord;
        this.lowerWord = lowerWord;
    }

    public String getUppperWord() {
        return uppperWord;
    }

    public String getLowerWord() {
        return lowerWord;
    }
}
